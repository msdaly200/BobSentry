def fail(msg):
    print(f"[ERROR] {msg}")
    print('RESULT: {"phase": "setup", "status": "failed", "reason": "' + msg + '"}')
    sys.exit(2)


# ── Step 1 — Admin token ─────────────────────────────────────────────────────
print("[SETUP] Obtaining admin token...", end=" ")
try:
    resp = requests.post(
        f"{BASE_URL}/realms/master/protocol/openid-connect/token",
        data={
            "grant_type": "password",
            "client_id": "admin-cli",
            "username": ADMIN_USER,
            "password": ADMIN_PASS,
        },
        timeout=15,
    )
    resp.raise_for_status()
except requests.exceptions.ConnectionError:
    fail("Cannot connect to Keycloak at localhost:8080 — is the container running?")
except requests.exceptions.HTTPError as e:
    fail(f"Admin token request failed: {e}")

