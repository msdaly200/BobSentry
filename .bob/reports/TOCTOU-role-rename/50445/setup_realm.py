print("OK")

# ── Step 2 — Create realm ────────────────────────────────────────────────────
print(f"[SETUP] Creating realm '{REALM}'...", end=" ")
resp = requests.post(
    f"{BASE_URL}/admin/realms",
    headers=auth,
    json={
        "realm": REALM,
        "enabled": True,
        "displayName": "Bob-Sentry Triage Realm",
        "registrationAllowed": False,
        "loginWithEmailAllowed": True,
        "duplicateEmailsAllowed": False,
        "resetPasswordAllowed": False,
        "editUsernameAllowed": False,
        "bruteForceProtected": False,
    },
    timeout=15,
)
if resp.status_code == 409:
    print("OK (already exists)")
elif resp.status_code == 201:
    print("OK")
else:
    fail(f"Create realm returned {resp.status_code}: {resp.text[:200]}")

# ── Step 3 — Create OIDC client ──────────────────────────────────────────────
print(f"[SETUP] Creating client '{CLIENT_ID}'...", end=" ")
resp = requests.post(
    f"{BASE_URL}/admin/realms/{REALM}/clients",
    headers=auth,
    json={
        "clientId": CLIENT_ID,
        "enabled": True,
        "protocol": "openid-connect",
        "publicClient": False,
        "secret": CLIENT_SECRET,
        "standardFlowEnabled": True,
        "directAccessGrantsEnabled": True,
        "serviceAccountsEnabled": False,
        "redirectUris": ["http://localhost:9999/*"],
        "webOrigins": ["http://localhost:9999"],
        "attributes": {
            "pkce.code.challenge.method": ""
        },
    },
    timeout=15,
)
if resp.status_code not in (201, 409):
    fail(f"Create client returned {resp.status_code}: {resp.text[:200]}")
