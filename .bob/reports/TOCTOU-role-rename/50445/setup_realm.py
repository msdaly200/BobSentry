# ── Step 4 — Resolve client UUID ─────────────────────────────────────────────
print(f"[SETUP] Resolving client UUID for '{CLIENT_ID}'...", end=" ")
resp = requests.get(
    f"{BASE_URL}/admin/realms/{REALM}/clients",
    headers=auth,
    params={"clientId": CLIENT_ID},
    timeout=15,
)
resp.raise_for_status()
clients = resp.json()
if not clients:
    fail(f"Client '{CLIENT_ID}' not found after creation")
client_uuid = clients[0]["id"]
print(f"OK ({client_uuid})")

# ── Step 5 — Create triage-user ──────────────────────────────────────────────
print(f"[SETUP] Creating user '{TEST_USER}'...", end=" ")
resp = requests.post(
    f"{BASE_URL}/admin/realms/{REALM}/users",
    headers=auth,
    json={
        "username": TEST_USER,
        "enabled": True,
        "email": "triage-user@localhost.test",
        "firstName": "Triage",
        "lastName": "User",
        "credentials": [
            {"type": "password", "value": TEST_PASS, "temporary": False}
        ],
    },
    timeout=15,
)
if resp.status_code == 409:
    print("OK (already exists)")
elif resp.status_code == 201:
    print("OK")
else:
    fail(f"Create triage-user returned {resp.status_code}: {resp.text[:200]}")

# Resolve triage-user UUID
