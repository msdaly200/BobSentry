import requests

# ── Configuration ────────────────────────────────────────────────────────────
BASE_URL          = "http://localhost:8080"
REALM             = "triage-realm"
CLIENT_ID         = "triage-client"
CLIENT_SECRET     = "triage-secret"
ADMIN_USER        = "admin"
ADMIN_PASS        = "admin"
TEST_USER         = "triage-user"
TEST_PASS         = "triage-pass"
DELEGATED_ADMIN   = "delegated-admin"
DELEGATED_PASS    = "delegated-pass"
